// explain the difference between getBy, findBy, queryBy:

//✍🏻 getBy: Searches for an element immediately and throws an error if not found; suitable for elements that are instantly available in the DOM.

//✍🏻 findBy: Searches for an element asynchronously, waiting for it to appear in the DOM; throws an error if not found; suitable for async operations.

//✍🏻 queryBy: Searches for an element immediately and does not throw an error (returns null if not found); suitable for checking the absence of an element.
